#include <stdio.h>

int main()
{
    int i = 10; // Declare and initialize "i" with 10
    int j = i;
    int a = 2, b = 3, c = 4, d = 5;
    // %d is called is formet specifier
    // %d is for int, %f is for float and %c is for char
    printf("\nThe value of i is %d and value j is %d", i, j);
    printf("\nThe value of i is %d and value b is %d", a, b);
    printf("\nThe value of i is %d and value d is %d", c, d);
    
    return 0;
}